dynamic add(num1,num2){
  
  return num1 + num2;

}

dynamic subtract(num1,num2){
  
  return num1 - num2;

}

dynamic multiplication(num1,num2){
  
  return num1 * num2;

}

dynamic divisioin(num1,num2){
  
  return num1 / num2;

}

void main(){

  var num1 = 10.87;
  var num2 = 90.43;
  print(add(num1, num2));
  print(subtract(num1, num2));
  print(multiplication(num1, num2));
  print(divisioin(num1, num2));

}